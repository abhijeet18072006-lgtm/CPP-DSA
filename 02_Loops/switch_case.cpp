#include <iostream>
using namespace std;
int main() {
    int num = 1;
    cout<<endl;
    switch(num){
        case 1 : cout<<"First case"<<endl;
            break;
        case 2 : cout<<"Character one"<<endl;
            break;
        default : cout<<"It is default case"<<endl;
    }
    cout<<endl;

    return 0;
}